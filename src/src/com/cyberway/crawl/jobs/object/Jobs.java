package com.cyberway.crawl.jobs.object;

import java.io.Serializable;
import java.util.Set;

import com.cyberway.cms.domain.Channel;
import com.cyberway.crawl.extractor.domain.Extract;

public class Jobs implements Serializable{

private static final long serialVersionUID = 7708805132868357321L;

    private Long oid;
	
	private String metaName;
	
	private String recovery;
	
	private String jobDescription;
	private String seeds;
	private boolean profile;
	private String jobUID;
	
	private Channel channel;
	
	private String status;
	
	private Set<Extract> extracts;
	
	private Extract keyFiledExtract;

	public Extract getKeyFiledExtract() {
		return keyFiledExtract;
	}

	public void setKeyFiledExtract(Extract keyFiledExtract) {
		this.keyFiledExtract = keyFiledExtract;
	}

	public Set<Extract> getExtracts() {
		return extracts;
	}

	public void setExtracts(Set<Extract> extracts) {
		this.extracts = extracts;
	}

	public String getJobUID() {
		return jobUID;
	}

	public void setJobUID(String jobUID) {
		this.jobUID = jobUID;
	}

	public boolean isProfile() {
		return profile;
	}

	public void setProfile(boolean profile) {
		this.profile = profile;
	}

	public String getJobDescription() {
		return jobDescription;
	}

	public void setJobDescription(String jobDescription) {
		this.jobDescription = jobDescription;
	}

	public String getRecovery() {
		return recovery;
	}

	public void setRecovery(String recovery) {
		this.recovery = recovery;
	}

	public String getSeeds() {
		return seeds;
	}

	public void setSeeds(String seeds) {
		this.seeds = seeds;
	}

	public String getMetaName() {
		return metaName;
	}

	public void setMetaName(String metaName) {
		this.metaName = metaName;
	}

	public Long getOid() {
		return oid;
	}

	public void setOid(Long oid) {
		this.oid = oid;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public Channel getChannel() {
		return channel;
	}

	public void setChannel(Channel channel) {
		this.channel = channel;
	}
}
