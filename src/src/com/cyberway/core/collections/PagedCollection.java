package com.cyberway.core.collections;



public interface PagedCollection<T> extends Iterable<T>
{
	int getFirstPage ();
	int getLastPage ();
	int getNextPage ();
	int getPrevPage ();
	int getCurrentPage ();
	void setCurrentPage (int currentPage);
    int getPageSize ();
    void setPageSize (int pageSize);
    int getPageCount ();
    int pageIndexOf (T value);
    int getCount ();
    int getVirtualCount ();
}
