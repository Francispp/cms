package com.cyberway.cms.survey.service;

import com.cyberway.core.dao.HibernateEntityDao;
import com.cyberway.cms.survey.domain.*;

public class SurveyService extends HibernateEntityDao<Questionnaire> {
	private QuestionnaireService QuestionnaireService;
	private QuestionService QuestionService;
	private SelectOptionService SelectOptionService;
	
	
	
	
	
	public QuestionnaireService getQuestionnaireService() {
		return QuestionnaireService;
	}
	public void setQuestionnaireService(QuestionnaireService questionnaireService) {
		QuestionnaireService = questionnaireService;
	}
	public QuestionService getQuestionService() {
		return QuestionService;
	}
	public void setQuestionService(QuestionService questionService) {
		QuestionService = questionService;
	}
	public SelectOptionService getSelectOptionService() {
		return SelectOptionService;
	}
	public void setSelectOptionService(SelectOptionService selectOptionService) {
		SelectOptionService = selectOptionService;
	}

}
