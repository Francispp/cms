package com.cyberway.cms.domain;


public class CmsBaseDocument
{
    private Long id;
    
	private Channel channel;//所属频道
	
	private  CmsSite site;//所属站点

    private Long numberOfClick = 0L;//点击数
    
	private int isInheritPerm=1;//是否继承父对象(频道)权限  1:继承, 0:单独 设置权限
    
    private String body;//正文内容 office控制内容
    
    private Integer isShare=0;
    
    private Long isHeadline;
    
	protected Long toped;
	protected Long newsed;
	protected Long firstpage;
	
	

	public Long getIsHeadline() {
		return isHeadline;
	}

	public void setIsHeadline(Long isHeadline) {
		this.isHeadline = isHeadline;
	}

	public Channel getChannel() {
		return channel;
	}

	public void setChannel(Channel channel) {
		this.channel = channel;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getNumberOfClick() {
		return numberOfClick;
	}

	public void setNumberOfClick(Long numberOfClick) {
		this.numberOfClick = numberOfClick;
	}

	public int getIsInheritPerm() {
		return isInheritPerm;
	}

	public void setIsInheritPerm(int isInheritPerm) {
		this.isInheritPerm = isInheritPerm;
	}

	public CmsSite getSite() {
		return site;
	}

	public void setSite(CmsSite site) {
		this.site = site;
	}

	public String getBody() {
		return body;
	}

	public void setBody(String body) {
		this.body = body;
	}

	public Integer getIsShare() {
		return isShare;
	}

	public void setIsShare(Integer isShare) {
		this.isShare = isShare;
	}

	public Long getFirstpage() {
		return firstpage;
	}

	public void setFirstpage(Long firstpage) {
		this.firstpage = firstpage;
	}

	public Long getNewsed() {
		return newsed;
	}

	public void setNewsed(Long newsed) {
		this.newsed = newsed;
	}

	public Long getToped() {
		return toped;
	}

	public void setToped(Long toped) {
		this.toped = toped;
	}

	
	
	
 
}
