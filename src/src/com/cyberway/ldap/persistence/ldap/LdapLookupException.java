package com.cyberway.ldap.persistence.ldap;

import com.cyberway.ldap.persistence.DataAccessException;


/**
 * Signifies an error interacting with LDAP
 * @author lrussell
 */
public class LdapLookupException extends DataAccessException {
	
	public LdapLookupException(){
	}
	
	public LdapLookupException(String s){
		super(s);
	}
	public LdapLookupException(Exception e){
		super(e);
	}
		

}
