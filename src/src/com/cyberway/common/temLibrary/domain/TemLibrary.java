package com.cyberway.common.temLibrary.domain;


import java.io.Serializable;
import java.util.Set;

/**
 * 【模版库】模块
 * 
 * @date 2012-03-09
 */
public class TemLibrary implements Serializable {

	private Long id;
	private String name;//库名
	private String remark; //说明
	private Long parentid;
	private Set subTypes;
	//private Set commonInfos;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public Long getParentid() {
		return parentid;
	}
	public void setParentid(Long parentid) {
		this.parentid = parentid;
	}
	public Set getSubTypes() {
		return subTypes;
	}
	public void setSubTypes(Set subTypes) {
		this.subTypes = subTypes;
	}

	

}
